import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { chromium } from 'playwright-core';

const repoRoot = dirname(dirname(fileURLToPath(import.meta.url)));
const draftsRoot = join(repoRoot, 'drafts');
const outputDir = join(repoRoot, 'public', 'css');
const origin = process.env.ANGORA_GENERATE_ORIGIN || 'http://127.0.0.1:4315';
const cssReadyTimeoutMs = Number(process.env.ANGORA_GENERATE_TIMEOUT_MS || 12000);

const header = `/**
 * ngx-angora-css Dynamic Styles
 *
 * This file is automatically populated by ngx-angora-css
 * with generated CSS rules based on the class names used in the application.
 *
 * Do not edit this file manually as it will be overwritten.
 */

/* This file is automatically generated by ngx-angora-css */
`;

const responsiveHeader = `/**
 * ngx-angora-css Dynamic Responsive Styles
 *
 * This file is automatically populated by ngx-angora-css
 * with generated CSS rules based on the class names used in the application.
 *
 * Do not edit this file manually as it will be overwritten.
 */

/* This file is automatically generated by ngx-angora-css */
`;

async function readJson(path) {
  return JSON.parse(await readFile(path, 'utf8'));
}

async function discoverTargets() {
  const entries = await readdir(draftsRoot, { withFileTypes: true });
  const targets = [];

  for (const entry of entries) {
    if (!entry.isDirectory() || entry.name === '_debug') continue;

    const siteConfigPath = join(draftsRoot, entry.name, 'site-config.json');
    let siteConfig;
    try {
      siteConfig = await readJson(siteConfigPath);
    } catch {
      continue;
    }

    const routes = Array.isArray(siteConfig.routes) && siteConfig.routes.length > 0
      ? siteConfig.routes
      : [{ path: '/' }];

    for (const route of routes) {
      const routePath = String(route?.path || '/');
      const url = new URL(routePath, origin);
      url.searchParams.set('draftDomain', entry.name);
      targets.push({
        domain: entry.name,
        path: routePath,
        url: url.toString(),
      });
    }
  }

  return targets.sort((left, right) => (
    left.domain.localeCompare(right.domain) || left.path.localeCompare(right.path)
  ));
}

async function launchBrowser() {
  try {
    return await chromium.launch({ channel: 'chrome', headless: true });
  } catch {
    return await chromium.launch({ headless: true });
  }
}

async function waitForAngoraCss(page) {
  const startedAt = Date.now();

  while (Date.now() - startedAt < cssReadyTimeoutMs) {
    const state = await page.evaluate(() => {
      const debug = window.__zlpAngoraDebug;
      const audit = debug?.stylesheetAudit?.(0);
      const summary = debug?.cssCreateSummary?.();

      return {
        hasDebugBridge: !!debug,
        totalRules: audit?.totalRules ?? 0,
        totalCreatedClasses: summary?.totalCreatedClasses ?? 0,
      };
    }).catch(() => ({ hasDebugBridge: false, totalRules: 0, totalCreatedClasses: 0 }));

    if (state.totalRules > 0 || state.totalCreatedClasses > 0) {
      return state;
    }

    await page.waitForTimeout(100);
  }

  throw new Error(`Timed out waiting for Angora CSS after ${ cssReadyTimeoutMs }ms`);
}

async function collectRules(page) {
  return await page.evaluate(() => {
    const getRules = (sheet) => {
      try {
        return Array.from(sheet.cssRules ?? [])
          .map((rule) => String(rule.cssText ?? '').trim())
          .filter(Boolean);
      } catch {
        return [];
      }
    };

    const normal = [];
    const responsive = [];

    for (const sheet of Array.from(document.styleSheets)) {
      const href = String(sheet.href ?? '');
      if (href.endsWith('/css/angora-styles.css') || href.includes('/css/angora-styles.css?')) {
        normal.push(...getRules(sheet));
      }

      if (href.endsWith('/css/angora-styles-responsive.css') || href.includes('/css/angora-styles-responsive.css?')) {
        responsive.push(...getRules(sheet));
      }
    }

    return { normal, responsive };
  });
}

function appendUnique(target, entries) {
  for (const entry of entries) {
    if (!target.seen.has(entry)) {
      target.seen.add(entry);
      target.rules.push(entry);
    }
  }
}

async function main() {
  const targets = await discoverTargets();
  if (targets.length === 0) {
    throw new Error(`No draft targets found in ${ draftsRoot }`);
  }

  const normal = { seen: new Set(), rules: [] };
  const responsive = { seen: new Set(), rules: [] };
  const browser = await launchBrowser();
  const results = [];

  try {
    const page = await browser.newPage({ viewport: { width: 1366, height: 900 } });

    for (const target of targets) {
      await page.goto(target.url, { waitUntil: 'domcontentloaded', timeout: 45000 });
      const state = await waitForAngoraCss(page);
      await page.evaluate(() => window.__zlpAngoraDebug?.runFullCssCreate?.()).catch(() => undefined);
      await page.waitForTimeout(150);
      await page.evaluate(() => window.scrollTo(0, Math.floor(document.body.scrollHeight * 0.75))).catch(() => undefined);
      await page.waitForTimeout(150);
      await page.evaluate(() => window.__zlpAngoraDebug?.runFullCssCreate?.()).catch(() => undefined);
      await page.waitForTimeout(150);

      const rules = await collectRules(page);
      appendUnique(normal, rules.normal);
      appendUnique(responsive, rules.responsive);

      results.push({
        ...target,
        totalRules: state.totalRules,
        totalCreatedClasses: state.totalCreatedClasses,
        collectedNormalRules: rules.normal.length,
        collectedResponsiveRules: rules.responsive.length,
      });
    }
  } finally {
    await browser.close();
  }

  await mkdir(outputDir, { recursive: true });
  await writeFile(join(outputDir, 'angora-styles.css'), `${ header }\n${ normal.rules.join('\n') }\n`, 'utf8');
  await writeFile(join(outputDir, 'angora-styles-responsive.css'), `${ responsiveHeader }\n${ responsive.rules.join('\n') }\n`, 'utf8');

  console.log(JSON.stringify({
    origin,
    targetCount: targets.length,
    normalRules: normal.rules.length,
    responsiveRules: responsive.rules.length,
    results,
  }, null, 2));
}

await main();
